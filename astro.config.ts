type AutogeneratedEntry = (
  | string
  | {
      label: string;
      translations?: Record<string, string> | undefined;
      items: any;
      collapsed: boolean;
    }
)[];

async function nestedAutogenerated(
  path: string,
  translations: Record<string, Record<string, string>>,
  priorize?: string[] | undefined,
): Promise<AutogeneratedEntry> {
  const full_path = "./src/content/docs/" + path;
  const dir = await readdir(full_path);
  let pages: AutogeneratedEntry = [];
  let folders: AutogeneratedEntry = [];
  for await (const item of dir) {
    if (item.endsWith(".md")) {
      pages.push(path + "/" + item.replace(".md", ""));
      continue;
    }
    if (item.endsWith(".mdx")) {
      pages.push(path + "/" + item.replace(".mdx", ""));
      continue;
    }
    if (translations && Object.hasOwn(translations, item)) {
      folders.push({
        label: translations[item].en,
        translations: translations[item],
        items: await nestedAutogenerated(
          path + "/" + item,
          translations,
          priorize,
        ),
        collapsed: true,
      });
      continue;
    }
    folders.push({
      label: item,
      items: await nestedAutogenerated(
        path + "/" + item,
        translations,
        priorize,
      ),
      collapsed: true,
    });
  }
  if (priorize) {
    pages.sort((a, b) => {
      return (
        priorize.findIndex((str) => b == str) +
        1 -
        (priorize.findIndex((str) => a == str) + 1)
      );
    });
  }
  return pages.concat(folders);
}

